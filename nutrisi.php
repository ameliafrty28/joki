            <h2 class="accordion">Apa itu Nutrisi?</h2>
            <div class="content">
                <p>Nutrisi merupakan elemen penting dalam kehidupan manusia untuk menjaga kesehatan tubuh. 
                Pemenuhan nutrisi yang seimbang meliputi karbohidrat, protein, lemak, vitamin, mineral, 
                dan air untuk mendukung fungsi tubuh yang optimal.</p>  
                <p>
                Kekurangan atau kelebihan nutrisi dapat menyebabkan berbagai masalah kesehatan, 
                seperti obesitas, malnutrisi, dan penyakit kronis lainnya. Oleh karena itu, penting untuk mengetahui kebutuhan nutrisi harian sesuai usia, berat badan, tinggi badan, dan aktivitas fisik.
                </p>

                <p>Masalah nutrisi atau malnutrisi adalah kondisi ketika tubuh tidak mendapatkan asupan gizi yang cukup atau seimbang.                 <p>
                Kekurangan atau kelebihan nutrisi dapat menyebabkan berbagai masalah kesehatan, 
                seperti obesitas, malnutrisi, dan penyakit kronis lainnya. Oleh karena itu, penting untuk mengetahui kebutuhan nutrisi harian sesuai usia, berat badan, tinggi badan, dan aktivitas fisik.
                </p>
                <p>Arti malnutrisi menurut WHO adalah kekurangan, kelebihan, atau ketidakseimbangan dalam asupan energi maupun nutrisi seseorang. Malnutrition dapat terjadi ketika seseorang memiliki terlalu banyak atau terlalu sedikit makanan dan nutrisi penting dalam tubuhnya. </p>
                <p>Secara umum, kondisi ini terbagi dalam 2 bentuk, yaitu terlalu sedikit nutrisi (kurang gizi) atau terlalu banyak nutrisi (overnutrition). </p>
                <p>Meskipun banyak terjadi pada anak-anak, tetapi sebenarnya malnutrisi tidak memandang usia karena dapat terjadi juga pada orang dewasa, baik itu laki-laki maupun wanita.</p>
                <p>Dampak malnutrisi ternyata dapat sangat mempengaruhi kesehatan karena menyebabkan penyakit dan komplikasi tertentu. Bahkan, dalam beberapa kasus, bisa menjadi mengancam jiwa.</p>
                <h2>Terdapat 4 bentuk malnutrisi, yaitu:</h2>
                <ul>
                    <li><span>Kurang gizi</span>
                        <p>yang meliputi wasting (berat badan rendah menurut tinggi badan), stunting (tinggi badan rendah menurut umur), dan kekurangan berat badan (berat badan rendah menurut umur). </p></li>
                    <li><span>Malnutrisi terkait mikronutrien</span>
                        <p>yang meliputi defisiensi mikronutrien (kekurangan vitamin dan mineral penting) maupun kelebihan mikronutrien </p></li>
                    <li><span>Kelebihan berat badan</span>
                        <p>obesitas</p></li>
                    <li><span>Penyakit tidak menular yang berhubungan dengan diet</span>
                        <p>seperti penyakit jantung, stroke, diabetes, dan beberapa jenis kanker.</p></li>
                </ul>
                <p>Akibat dari malnutrisi mungkin seseorang akan mengalami masalah kesehatan jangka pendek dan jangka panjang, pemulihan yang lambat dari luka dan penyakit, dan risiko infeksi yang lebih tinggi.</p>
                
            </div>

            <h2 class="accordion">Klasifikasi Berat Badan</h2>
                <div class="content">
                <p>Klasifikasi berat badan berdasarkan Indeks Massa Tubuh (IMT) yang direkomendasikan oleh WHO :</p>
                <ul>
                    <li><span>Underweight </span></li>
                    <li><span>Normal</span></li>
                    <li><span>Overweight</span></li>
                    <li><span>Obesitas</span></li>
                </ul>
                <p>Cara menghitung IMT adalah dengan cara membagikan Berat Badan (BB) dengan Tinggi Badan (TB), maka akan menghasilkan nilai IMT.</p>
                <img src="img/malnutrisi.jpg" alt="IMT manusia" class="img-content">
                <h2 class="accordion">Underweight</h2> 
                <div class="content">
                    <h2>Underweight (Kekurangan Berat Badan): IMT < 18,5</h2>
                    <p>Underweight atau berat badan kurang adalah kondisi ketika berat badan seseorang berada di bawah rentang normal untuk usianya. Underweight merupakan salah satu masalah gizi yang dapat memengaruhi kesehatan secara serius.</p>
                    <p>Underweight dapat disebabkan oleh kurangnya asupan zat gizi yang masuk ke dalam tubuh. Kondisi ini dapat memengaruhi sistem kekebalan tubuh, kekuatan tulang, dan kesehatan tubuh secara keseluruhan.</p>
                    <p>Untuk orang dewasa, berat badan yang normal diukur berdasarkan Indeks Massa Tubuh (IMT). Menurut WHO, <span>berat badan yang normal untuk orang dewasa adalah dengan IMT antara 18,5–24,9.</span></p>
                    <h2>Penyebab</h2>
                    <ul>
                        <li>Genetik</li>
                        <li>Metabolisme tinggi</li>
                        <li>Sering melakukan aktivitas fisik</li>
                        <li>Menderita penyakit tertentu</li>
                        <li>Gangguan kesehatan mental</li>
                    </ul>

                    <h2>Tanda Gejala</h2>
                    <ul>
                        <li>Tubuh tampak kurus</li>
                        <li>Rambut mudah rontok</li>
                        <li>Sistem kekebalan tubuh lemah</li>
                        <li>Mudah lelah</li>
                        <li>Sulit berkonsentrasi</li>
                        <li>Kurang bertenaga saat melakukan aktivitas</li>
                        <li>Tulang cenderung rapuh</li>
                        <li>Pertumbuhan dan perkembangan yang lambat</li>
                    </ul>
                    <p>Rekomendasi Pola Hidup dan Aktivitas Pisik</p>
                    <ul>
                        <li><span>Pola Hidup, </span>fokus pada peningkatan asupan kalori melalui makanan bernutrisi seperti protein, karbohidrat kompleks, lemak sehat, dan sayur/buah.
                        </li>
                        <li><span>Jadwal Makan,</span> 5-6 kali sehari dalam porsi kecil tetapi bernutrisi tinggi.
                        </li>
                        <li><span>Aktivitas Fisik,</span> olahraga dengan intensitas ringan seperti yoga, pilates, atau latihan kekuatan untuk meningkatkan massa otot.
                        </li>
                    </ul>
                </div>

                <h2 class="accordion">Normal</h2> 
                <div class="content">
                    <h2>Normal: IMT 18,5–24,9</h2>
                    <p>Berat badan normal berdasarkan IMT adalah IMT 18,5 - 24,9.</p>
                    <p>Rekomendasi Pola Hidup dan Aktivitas Pisik</p>
                    <ul>
                        <li><span>Pola Hidup, </span>pertahankan pola makan seimbang yang mencakup karbohidrat kompleks, protein tanpa lemak, lemak sehat, dan asupan sayur/buah sesuai kebutuhan kalori.
                        </li>
                        <li><span>Jadwal Makan,</span> 3 kali makan utama dengan 2-3 camilan sehat di antara waktu makan.
                        </li>
                        <li><span>Aktivitas Fisik,</span> 150 menit aktivitas aerobik sedang per minggu (misalnya jalan cepat, jogging), dengan tambahan latihan kekuatan 2 kali per minggu.
                        </li>
                    </ul>
                </div>

                <h2 class="accordion">Overweight</h2> 
                <div class="content">
                    <h2>Overweight (Kelebihan Berat Badan): IMT 25,0–29,9</h2>
                    <p>Overweight adalah kondisi ketika berat badan seseorang melebihi berat badan ideal. Berat badan berlebih ini bisa disebabkan oleh penumpukan lemak, air, tulang, atau otot.</p>
                    <p>Overweight berbeda dengan obesitas, meskipun keduanya sama-sama ditandai dengan bentuk badan yang lebih besar dari ukuran ideal. Perbedaannya adalah, obesitas merupakan kelainan atau penyakit yang ditandai dengan penimbunan lemak tubuh secara berlebihan.</p>
                    
                    <h2>Penyebab</h2>
                    <ul>
                        <li>Genetik</li>
                        <li>Kurangnya melakukan aktivitas fisik</li>
                        <li>Konsumsi makanan rendah nutrisi dan tinggi kalori, lemak, dan gula</li>
                    </ul>

                    <h2>Tanda Gejala</h2>
                    <ul>
                        <li>Kelelahan</li>
                        <li>Nyeri punggung</li>
                        <li>Nyeri otot dan sendi</li>
                        <li>Gangguan metabolisme</li>
                        <li>Diabetes tipe 2</li>
                        <li>Kolestrol tinggi</li>
                        <li>Tekanan darah tinggi</li>
                        <li>Depresi</li>
                    </ul>

                    <p>Rekomendasi Pola Hidup dan Aktivitas Pisik</p>
                    <ul>
                        <li><span>Pola Hidup, </span>kurangi konsumsi makanan tinggi gula, lemak jenuh, dan makanan olahan. Fokus pada diet rendah kalori yang tetap seimbang.
                        </li>
                        <li><span>Jadwal Makan,</span> 3 kali makan utama dengan camilan rendah kalori (seperti buah segar atau kacang-kacangan) jika diperlukan.
                        </li>
                        <li><span>Aktivitas Fisik,</span> 300 menit aktivitas aerobik per minggu, seperti berenang, bersepeda, atau berjalan kaki, ditambah latihan kekuatan untuk meningkatkan metabolisme.                </li>
                    </ul>
                </div>

                <h2 class="accordion">Obesitas</h2> 
                <div class="content">
                    <h2>Obesitas: IMT ≥ 30.</h2>
                    <p>Obesitas menurut Organisasi Kesehatan Dunia (WHO) adalah penumpukan lemak yang abnormal atau berlebihan yang menimbulkan risiko bagi kesehatan'. Pengukuran menggunakan dengan Indeks Massa Tubuh (IMT). Selain itu terdapat metode lain dengan menggunakan rasio lingkar pinggang dan tinggi badan yang, jika digunakan bersama IMT akan didapat hasil yang lebih akurat.</p>        

                    <h2>Penyebab</h2>
                    <ul>
                        <li>Genetik</li>
                        <li>Kurangnya melakukan aktivitas fisik</li>
                        <li>Konsumsi makanan rendah nutrisi dan tinggi kalori, lemak, dan gula</li>
                    </ul>

                    <h2>Tanda Gejala</h2>
                    <ul>
                        <li>Tumpukan lemak di bagian payudara</li>
                        <li>Stretch mark di punggung dan pinggul</li>
                        <li>Kulit menebal dan menjadi lebih gelap.</li>
                    </ul>

                    <h2>Pencegahan</h2>
                    <ul>
                        <li>Gerakan Masyarakat Hidup Sehat (GERMAS) dan Gerakan Nusantara Tekan Angka Obesitas (Gentas)</li>
                        <li>Konsumsi makanan sehat dan gizi seimbang, konsumsi buah sayur minimal 5 porsi per hari.</li>
                        <li>Konsumsi gula, garam dan lemak dengan pedoman G4 G1 L5 (konsumsi Gula maksimal 4 sendok makan atau 50 gram per hari, konsumsi Garam maksimal  1 sendok teh atau 2 gram per hari, konsumsi Lemak maksimal  5 sendok makan atau 67 gram per hari)</li>
                        <li>Menjaga pola makan dan meningkatkan aktivitas fisik.</li>
                        <li>Implementasi perilaku CERDIK. CERDIK ini mempunyai makna, Cek kesehatan secara berkala, Enyahkan asap rokok, Rajin aktivitas fisik, Diet sehat dan seimbang, Istirahat cukup, Kelola stress.</li>
                    </ul>
                </div>
            </div>

            <h2 class="accordion">IMT(Indeks Massa Tubuh)</h2>
            <div class="content">    
            <p>Indeks Massa Tubuh (IMT) adalah salah satu cara sederhana untuk menilai status gizi seseorang berdasarkan berat badan dan tinggi badannya. IMT digunakan sebagai indikator umum untuk mengidentifikasi apakah seseorang memiliki berat badan normal, kekurangan berat badan, kelebihan berat badan, atau obesitas. Dalam fitur ini pengguna hanya memasukkan tinggi badan dan berat badan, kemudian web ini akan menganalisa apakah berat badan pengguna tersebut normal atau tidak.</p>
            </div>


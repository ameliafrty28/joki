            <h2 class="accordion">Menstruasi</h2>
            <div class="content">   
                <h2 class="accordion">Pengertian</h2> 
                <div class="content">   
                    <p>Menstruasi atau haid adalah proses keluarnya darah dan sel-sel tubuh dari vagina yang terjadi secara periodik setiap bulan. Menstruasi merupakan bagian dari proses normal organ reproduksi wanita untuk mempersiapkan kehamilan.</p>
                    <p>Menstruasi menandai dimulainya pubertas dan kemampuan seorang wanita untuk mengandung anak. Siklus menstruasi pertama biasanya dimulai pada usia 12 tahun, atau 2–3 tahun setelah payudara tumbuh. Siklus ini akan terus berlangsung hingga memasuki masa menopause.</p>        
                    
                    <h2 class="accordion">Ciri-Ciri</h2>
                    <p>Ciri-ciri menstruasi yang normal adalah:</p>
                    <ul>
                        <li>Siklusnya terjadi dalam kisaran 21-35 hari</li>
                        <li>Berlangsung selama 2-7 hari</li>
                    </ul>
                    <p>Namun tidak semua wanita memiliki siklus menstruasi yang sama, apabila siklus terjadi lebih singkat atau lebih lama dari batas tersebut kamu mungkin mengalami gangguan siklus menstruasi. Sangat disarankan untuk berkonsultasi dengan dokter untuk mengetahui masalah siklus menstruasimu.</p>
                </div>
                <h2 class="accordion">Gejala Yang Terjadi Saat Masa Menstruasi</h2>
                <div class="content">   
                    <ul>
                        <li>Perut terasa mulas, mual dan panas</li>
                        <li>Terasa nyeri saat buang air kecil</li>
                        <li>Tubuh tidak fit</li>
                        <li>Demam</li>
                        <li>Sakit kepala dan pusing</li>
                        <li>Mood swing</li>
                    </ul>

                    <p>Pada menstruasi atau haid ada istilah yang disebut DISMINORE yaitu, istilah medis untuk nyeri haid atau kram perut yang terjadi saat menstruasi. Dismenore merupakan keluhan ginekologi yang umum terjadi pada wanita usia muda.</p>
                    <p>Untuk mengetahui tingkat disminore yang dialami, pengguna dapat menjawab pertanyaan berikut:</p>
                    <ol>
                        <li>Apakah anda merasakan nyeri ringan di perut bagian bawah dan berlangsung singkat?</li>
                        <li>Pada saat menstruasi berapa skla nyeri yang kamu rasakan dari 0-10 (0=ringan, 10= berat)?</li>
                        <li>Apakah nyeri menstruasi yang anda rasakan sering mengganggu aktifitas fisik mu?</li>
                        <li>Apakah anda merasakan nyeri yang menjalar ke punggung bawah disertai mual ringan, Lelah dan mood swings</li>
                        <li>Apakah anda merasakan nyeri perut bagian bawah, muntah, diare, pingsam atau sakit kepala?</li>
                        <li>Apakah nyeri yang kamu rasakan sering membuat kamu sulit untuk berkonsetrasi dalam melakukan aktifitas sehari-harimu?</li>
                    </ol>
                    <p>Jika nyeri dismenore masih ringan, gejalanya meliputi nyeri ringan di perut bagian bawah yang berlangsung singkat dan tidak mengganggu aktivitas. Penanganannya bisa dilakukan dengan istirahat, kompres hangat, atau konsumsi obat pereda nyeri ringan serta olahraga ringan.</p>
                    <p>Jika nyeri sedang, bisa diatasi dengan obat pereda nyeri seperti ibuprofen, pijat ringan di area perut bawah, dan konsumsi air jahe.</p>
                    <p>Untuk nyeri yang lebih intens, yang mengganggu aktivitas sehari-hari dan tidak bisa diatasi dengan obat pereda nyeri, bisa terkait dengan kondisi seperti endometriosis atau adenomiosis. Penanganan untuk nyeri berat membutuhkan evaluasi medis lebih lanjut, termasuk konsultasi dengan dokter untuk terapi hormonal dan penggunaan obat pereda nyeri.</p>

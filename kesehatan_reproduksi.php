            <h2 class="accordion">Apa itu Kesehatan Reproduksi?</h2>
            <div class="content">
                <p>Kesehatan reproduksi adalah keadaan sehat secara fisik, mental, dan sosial yang utuh dalam segala aspek yang berhubungan dengan sistem reproduksi, fungsi, dan prosesnya. Kesehatan reproduksi tidak hanya bebas dari penyakit atau kecacatan.</p>
                <p>Terdapat beberapa penyakit atau permasalahan pada kesehatan reproduksi, di antaranya seperti IMS (Infeksi Menular Seksual) dan Kanker Serviks.</p>
            </div>

            <h2 class="accordion">IMS (Infeksi Menular Seksual)</h2>
            <div class="content">    
                <h2 class="accordion">Pengertian</h2> 
                <div class="content">       
                    <p>Infeksi Menular Seksual (IMS) adalah infeksi yang ditularkan melalui hubungan seksual, baik melalui vagina, mulut, maupun anus. Infeksi tersebut dapat disebabkan oleh bakteri (misalnya sifilis), jamur, virus (misalnya herpes, HIV), atau parasit (misalnya kutu).
                    </p>
                </div>

                <h2 class="accordion">Tanda Gejala</h2>
                <div class="content">
                    <h3>Pada Perempuan:</h3>
                    <ul>
                        <li>Luka dengan atau tanpa rasa sakit di sekitar alat kelamin, anus, mulut atau bagian tubuh ang lain, tonjolan kecil-kecil, diikuti luka yang sangat sakit disekitar alat kelamin.</li>
                        <li>Cairan tidak normal yaitu cairan dari vagina bisa gatal, kekuningan, kehijauan, berbau atau berlendir.</li>
                        <li>Sakit pada saat buang air kecil yaitu IMS pada wanita biasanya tidak menyebabkan sakit atau burning urination.</li>
                        <li>Tonjolan seperti jengger ayam yang tumbuh disekitar alat kelamin.</li>
                        <li>Sakit pada bagian bawah perut yaitu rasa sakit yang hilang muncul dan tidak berkaitan dengan menstruasi bisa menjadi tanda infeksi saluran reproduksi (infeksi yang telah berpindah kebagian dalam sistemik reproduksi, termasuk tuba fallopi dan ovarium).</li>
                    </ul>
                    <h3>Pada Laki-Laki:</h3>
                    <ul>
                        <li>Luka dengan atau tanpa rasa sakit di sekitar alat kelamin, anus, mulut atau bagian tubuh yang lain, tonjolan kecil-kecil, diikuti luka yang sangat sakit di sekitar alat kelamin.</li>
                        <li>Cairan tidak normal yaitu cairan bening atau bewarna berasal dari pembukaan kepala penis atau anus.</li>
                        <li>Sakit pada saat buang air kecil yaitu rasa terbakar atau rasa sakit selama atau setelah urination.</li>
                        <li>Kemerahan pada sekitar alat kelamin, kemerahan dan sakit di kantong zakar.</li>

                    </ul>
                </div>
                
                <h2 class="accordion">Pencegahan</h2>
                <div class="content">
                    <p>WHO menyatakan bahwa pantang dari hubungan seksual (abstinence) dan inisiasi tertunda perilaku seksual (terutama menghindari seks pranikah) adalah beberapa komponen utama dari upaya pencegahan IMS bagi kaum muda. Monogami dan pengurangan jumlah pasangan seksual (be faithful) serta meningkatkan akses dan layanan pencegahan komprehensif, termasuk pendidikan pencegahan dan penyediaan kondom (condoms) sangat penting bagi orang-orang muda yang aktif secara seksual.
                    Pada dasarnya remaja perlu memahami pentingnya poin-poin edukasi tentang hal-hal berikut ini:
                    </p>
                    <ol>
                        <li>Menghindari perilaku seksual yang tidak bertanggung jawab, antara lain: hubungan seksual pranikah, hubungan seksual tanpa pengaman.</li>
                        <li>Meningkatkan ketahanan moral melalui pendidikan agama/kerohanian. Di banyak ajaran agama di dunia, hubungan seksual pranikah adalah salah satu bentuk zina yang merupakan dosa besar.</li>
                        <li>Melakukan kegiatan-kegiatan positif, agar tidak terlintas untuk melakukan hubungan seksual.</li>
                        <li>Mencari informasi yang benar sebanyak mungkin tentang risiko tertular IMS.</li>
                        <li>Mendiskusikan dengan orang tua, guru, atau teman mengenai hal-hal yang berkaitan dengan perilaku seksual, dan mendorong untuk tidak malu bertanya.</li>
                        <li>Menolak ajakan pasangan yang meminta untuk melakukan hubungan seksual tidak aman.</li>
                        <li>Bersikap waspada, mampu membaca situasi, dan melindungi diri. Misalnya ketika pergi ke pesta atau clubbing, usahakan pergi bersama teman atau anggota keluarga yang dapat dipercaya. Minum minuman yang memabukkan (baik disengaja maupun tidak) juga merupakan gerbang menuju perilaku seksual tidak aman.</li>
                        <li>Mengetahui hak atas serta cara untuk menjaga kesehatan reproduksinya. Misalnya penggunaan kondom.</li>
                    </ol>
                    <p>Perlu diperhatikan bahwa edukasi pencegahan IMS pada remaja harus disesuaikan dengan kelompok target. Edukasi pada remaja pada umumnya mungkin akan sedikit berbeda dengan edukasi pada remaja dari kelompok YKAP (young key affected population). YKAP terdiri dari remaja pekerja seks, pengguna jasa pekerja seks, kaum transjender, pengguna obat suntik, pria pelaku seks sesama jenis, dan orang yang terinfeksi HIV. Tidak semua poin dapat diaplikasikan secara efektif pada setiap situasi. Sebagai contoh, poin (h) tentang hak dan cara menjaga sistem reproduksi mungkin patut lebih diprioritaskan oleh konselor/ pendidik dalam memberi edukasi pada kelompok remaja pekerja seks. Beberapa poin bisa menjadi isu yang sensitif bagi kelompok tertentu. Oleh karena itu, pemilihan cara edukasi harus dipersiapkan dan dipertimbangkan secara bijaksana.
                    </p>

            <h2 class="accordion">Apa itu Kesehatan Reproduksi?</h2>
            <div class="content">
                <p>Kesehatan reproduksi adalah keadaan sehat secara fisik, mental, dan sosial yang utuh dalam segala aspek yang berhubungan dengan sistem reproduksi, fungsi, dan prosesnya. Kesehatan reproduksi tidak hanya bebas dari penyakit atau kecacatan. Terdapat beberapa penyakit atau permasalahan pada kesehatan reproduksi, di antaranya seperti IMS (Infeksi Menular Seksual) dan Kanker Serviks.</p>
            </div>

            <h2 class="accordion">IMS (Infeksi Menular Seksual)</h2>
            <div class="content">    
                <h2 class="accordion">Pengertian</h2> 
                <div class="content">       
                    <p>Infeksi Menular Seksual (IMS) adalah infeksi yang ditularkan melalui hubungan seksual, baik melalui vagina, mulut, maupun anus. Infeksi tersebut dapat disebabkan oleh bakteri (misalnya sifilis), jamur, virus (misalnya herpes, HIV), atau parasit (misalnya kutu).
                    </p>
                </div>

                <h2 class="accordion">Tanda Gejala</h2>
                <div class="content">
                    <h2>Pada Perempuan:</h2>
                    <ul>
                        <li>Luka dengan atau tanpa rasa sakit di sekitar alat kelamin, anus, mulut atau bagian tubuh ang lain, tonjolan kecil-kecil, diikuti luka yang sangat sakit disekitar alat kelamin.</li>
                        <li>Cairan tidak normal yaitu cairan dari vagina bisa gatal, kekuningan, kehijauan, berbau atau berlendir.</li>
                        <li>Sakit pada saat buang air kecil yaitu IMS pada wanita biasanya tidak menyebabkan sakit atau burning urination.</li>
                        <li>Tonjolan seperti jengger ayam yang tumbuh disekitar alat kelamin.</li>
                        <li>Sakit pada bagian bawah perut yaitu rasa sakit yang hilang muncul dan tidak berkaitan dengan menstruasi bisa menjadi tanda infeksi saluran reproduksi (infeksi yang telah berpindah kebagian dalam sistemik reproduksi, termasuk tuba fallopi dan ovarium).</li>
                    </ul>
                </div>
                
                <h2 class="accordion">Pencegahan</h2>
                <div class="content">
                    <p>WHO menyatakan bahwa pantang dari hubungan seksual (abstinence) dan inisiasi tertunda perilaku seksual (terutama menghindari seks pranikah) adalah beberapa komponen utama dari upaya pencegahan IMS bagi kaum muda. Monogami dan pengurangan jumlah pasangan seksual (be faithful) serta meningkatkan akses dan layanan pencegahan komprehensif, termasuk pendidikan pencegahan dan penyediaan kondom (condoms) sangat penting bagi orang-orang muda yang aktif secara seksual.
                    Pada dasarnya remaja perlu memahami pentingnya poin-poin edukasi tentang hal-hal berikut ini:
                    </p>
                    <ol>
                        <li>Menghindari perilaku seksual yang tidak bertanggung jawab, antara lain: hubungan seksual pranikah, hubungan seksual tanpa pengaman.</li>
                        <li>Meningkatkan ketahanan moral melalui pendidikan agama/kerohanian. Di banyak ajaran agama di dunia, hubungan seksual pranikah adalah salah satu bentuk zina yang merupakan dosa besar.</li>
                        <li>Melakukan kegiatan-kegiatan positif, agar tidak terlintas untuk melakukan hubungan seksual.</li>
                        <li>Mencari informasi yang benar sebanyak mungkin tentang risiko tertular IMS.</li>
                        <li>Mendiskusikan dengan orang tua, guru, atau teman mengenai hal-hal yang berkaitan dengan perilaku seksual, dan mendorong untuk tidak malu bertanya.</li>
                        <li>Menolak ajakan pasangan yang meminta untuk melakukan hubungan seksual tidak aman.</li>
                        <li>Bersikap waspada, mampu membaca situasi, dan melindungi diri. Misalnya ketika pergi ke pesta atau clubbing, usahakan pergi bersama teman atau anggota keluarga yang dapat dipercaya. Minum minuman yang memabukkan (baik disengaja maupun tidak) juga merupakan gerbang menuju perilaku seksual tidak aman.</li>
                        <li>Mengetahui hak atas serta cara untuk menjaga kesehatan reproduksinya. Misalnya penggunaan kondom.</li>
                    </ol>   
                    <p>Pencegahan IMS dapat dilakukan dengan imunisasi HPV. Hal ini merupakan pencegahan primer sebaiknya dilakukan sedini mungkin dan dapat diberikan mulai remaja putri berusia 10 tahun (sebelum mereka memulai hubungan seksual) sampai dengan usia 55 tahun. Jadwal pemberian vaksin yaitu pemberian bulan 0, 1 atau 2, dan 6.</p>         
                    <br><p>Perlu diperhatikan bahwa edukasi pencegahan IMS pada remaja harus disesuaikan dengan kelompok target. Edukasi pada remaja pada umumnya mungkin akan sedikit berbeda dengan edukasi pada remaja dari kelompok YKAP (young key affected population). YKAP terdiri dari remaja pekerja seks, pengguna jasa pekerja seks, kaum transjender, pengguna obat suntik, pria pelaku seks sesama jenis, dan orang yang terinfeksi HIV. Tidak semua poin dapat diaplikasikan secara efektif pada setiap situasi. Sebagai contoh, poin (h) tentang hak dan cara menjaga sistem reproduksi mungkin patut lebih diprioritaskan oleh konselor/ pendidik dalam memberi edukasi pada kelompok remaja pekerja seks. Beberapa poin bisa menjadi isu yang sensitif bagi kelompok tertentu. Oleh karena itu, pemilihan cara edukasi harus dipersiapkan dan dipertimbangkan secara bijaksana.
                    </p>
                </div>

                <h2 class="accordion">Komplikasi</h2>
                <div class="content">
                    <p>Komplikasi yang mungkin terjadi, diantaranya:</p>
                    <ol>
                        <li>Radang panggul pada perempuan</li>
                        <li>Kemungkinan terjadi kemandulan baik pada perempuan atau laki-laki</li>
                        <li>Infeksi mata pada bayi baru lahir yang dapat menyebabkan kebutaan</li>
                        <li>Kehamilan ektopik (di luar kandungan) dan memudahkan penularan infeksi HIV</li>
                        <li>Penyakit sifilis disebut juga raja singa, disebabkan bakteri Treponema pallidum</li>
                    </ol>
                </div>
                
                <h2 class="accordion">Pengobatan</h2>
                <div class="content">
                <h2>Pengobatan IMS</h2>
                    <p>Infeksi Menular Seksual (IMS) dapat diobati sesuai dengan jenis infeksinya. Untuk infeksi bakteri seperti klamidia, gonore, dan sifilis, pengobatan utama biasanya menggunakan antibiotik, seperti azitromisin, doksisiklin, atau suntikan penisilin. Jika IMS disebabkan oleh virus seperti HIV, herpes genital, atau hepatitis B, pengobatan dilakukan dengan obat antivirus untuk mengendalikan gejala dan mencegah komplikasi.</p>
                    <br><p>Beberapa IMS, seperti HPV, tidak dapat disembuhkan sepenuhnya, tetapi gejala seperti kutil kelamin dapat diatasi dengan prosedur medis seperti krioterapi atau laser. Untuk infeksi parasit seperti trikomoniasis, obat yang digunakan adalah metronidazol atau tinidazol.</p>
                    <br><p>Selama pengobatan, sangat penting untuk menghindari hubungan seksual hingga dinyatakan sembuh oleh dokter, serta menganjurkan pasangan seksual untuk menjalani pemeriksaan dan pengobatan guna mencegah reinfeksi. Sebagai langkah pencegahan, gunakan kondom saat berhubungan seksual dan, jika memungkinkan, lakukan vaksinasi seperti vaksin HPV dan hepatitis B.</p>
                    <br><p>Jika Anda mengalami gejala seperti nyeri, luka, atau keputihan yang tidak biasa, segera periksakan diri ke dokter untuk mendapatkan penanganan yang tepat.</p>
            </div>

            <h2 class="accordion">Kanker Serviks</h2>
            <div class="content">            
                <h2 class="accordion">Pengertian</h2>    
                <div class="content">    
                    <p>Serviks: bagian rahim yang terdapat pada puncak vagina (liang sanggama) yang hanya dapat dilihat dengan alat (spekulum). Serviks menghubungkan uterus (rahim) ke vagina. Selama haid, darah mengalir dari rahim melalui leher rahim ke dalam vagina. Vagina mengeluarkan darah haid ke bagian luar tubuh. Serviks menghasilkan lendir. Saat berhubungan seks, lendir membantu sperma bergerak dari vagina melalui serviks ke dalam rahim. 
                        <br> <br>Selama kehamilan serviks tertutup rapat untuk membantu menjaga bayi di dalam uterus. Saat proses persalinan serviks terbuka untuk memungkinkan bayi melewati vagina. Kanker Serviks adalah Penyakit tumor ganas di leher rahim yang dapat menyebar ke organ-organ yang lain dan menyebabkan kematian.</p>
                </div>

                <h2 class="accordion">Tanda Gejala</h2>
                <div class="content">
                    <ol type="a">
                        <li>Ada bercak atau pendarahan setelah hubungan seksual</li>
                        <li>Ada bercak atau pendarahan di luar masa haid</li>
                        <li>Ada bercak atau pendarahan pada masa menopause</li>
                        <li>Mengalami masa haid yang lebih berat dan lebih panjang dari biasanya</li>
                        <li>Keluarnya bau menyengat yang tidak bisa dihilangakan walau- pun sudah diobati.</li>
                    </ol>
                </div>
                
                <h2 class="accordion">Yang Mempunyai Risiko Tinggi Menderita Kanker Serviks</h2>
                <div class="content">
                    <ul>
                        <li>Aktivitas seksual sebelum usia 20 tahun</li>
                        <li>Berganti-ganti pasangan seksual</li>
                        <li>Terpapar infeksi yang ditularkan secara seksual (IMS)</li>
                        <li>Ibu atau kakak yang menderita kanker serviks</li>
                        <li>Papsmear sebelumnya yang abnormal</li>
                        <li>Perokok aktif maupun pasif</li>
                        <li>Penurunan daya tahan tubuh penyakit human immu- nodeficiency virus (HIV) atau acquired immunodeficiency syndrome (AIDS)</li>
                        <li>penggunaan kortikosteroid lama</li>
                        <li>Menggunakan pil keluarga berencana (KB) untuk waktu yang lama (5 tahun atau lebih) Namun, risiko menurun ketika wanita berhenti menggunakan pil KB</li>
                        <li>Memiliki banyak anak (5 atau lebih)</li>
                    </ul>
                </div>

                <h2 class="accordion">Pencegahan</h2>
                <div class="content">
                    <p>Menerapkan pola hidup CERDIK, yaitu:</p>
                    <ol>
                        <li>Cek Kesehatan secara berkala</li>
                        <li>Enyakan dari lingkungan asap rokok; Mengenal Sejak Dini Kanker Serviks</li>
                        <li>Rajin aktivitas fisik, seperti olah raga rutin</li>
                        <li>Diet sehat dengan menu seimbang</li>
                        <li>Istirahat cukup, jangan begadang</li>
                        <li>Kelola stress, seperti dengan rekreasi, ibadah.</li>
                    </ol>
                    <h3>Imunisasi HPV</h3>
                    <p>Hal ini merupakan pencegahan primer sebaiknya dilakukan sedini mungkin dan dapat diberikan pada remaja putri usia 9 hingga 14 tahun, sebelum mereka menjadi aktif secara seksual, agar lebih efektif sampai dengan usia 55 tahun. Jadwal pemberian vaksin yaitu pemberian bulan 0, 1 atau 2, dan 6.</p>
                    <br><p>Remaja putri perlu didorong untuk berbicara dengan keluarga, teman, atau seorang konselor untuk mendapatkan dukungan emosional. Keterbukaan sangat penting untuk mengurangi kecemasan dan stres. Selain pengobatan fisik, penting juga untuk menjaga kesehatan mental. Cemas dan stres adalah hal yang wajar, tetapi berbicara tentang perasaan, baik dengan keluarga atau profesional, bisa membantu mengatasi perasaan tersebut dan menjaga keseimbangan emosional selama pengobatan.
                    </p>
                    <p>Edukasi yang diberikan harus menyentuh aspek fisik, emosional, dan sosial dari pengalaman remaja putri yang menghadapi kanker serviks, serta pentingnya pencegahan sejak dini melalui vaksinasi, pemeriksaan, dan pola hidup sehat.</p>
                </div>

                <h2 class="accordion">Pengobatan</h2>
                <div class="content">
                    <p>Pengobatan kanker serviks tergantung pada stadium penyakitnya. Pada tahap awal, operasi seperti histerektomi atau konisasi sering dilakukan. Untuk stadium lanjut, radioterapi dan kemoterapi digunakan untuk menghancurkan sel kanker, dan terapi target atau imunoterapi dapat membantu jika kanker telah menyebar. Pada stadium akhir, perawatan paliatif fokus mengurangi gejala dan meningkatkan kualitas hidup.</p>
                </div>
            </div>

            <h2 class="accordion">Menstruasi</h2>
            <div class="content">   
                <h2 class="accordion">Pengertian</h2> 
                <div class="content">   
                    <p>Menstruasi atau haid adalah proses keluarnya darah dan sel-sel tubuh dari vagina yang terjadi secara periodik setiap bulan. Menstruasi merupakan bagian dari proses normal organ reproduksi wanita untuk mempersiapkan kehamilan. Menstruasi menandai dimulainya pubertas dan kemampuan seorang wanita untuk mengandung anak. Siklus menstruasi pertama biasanya dimulai pada usia 12 tahun, atau 2–3 tahun setelah payudara tumbuh. Siklus ini akan terus berlangsung hingga memasuki masa menopause.</p>        
                    </div>
                
                    <h2 class="accordion">Ciri-Ciri</h2>
                    <div class="content">   

                    <p>Ciri-ciri menstruasi yang normal adalah:</p>
                    <ul>
                        <li>Siklusnya terjadi dalam kisaran 21-35 hari</li>
                        <li>Berlangsung selama 2-7 hari</li>
                    </ul>
                    <p>Namun tidak semua wanita memiliki siklus menstruasi yang sama, apabila siklus terjadi lebih singkat atau lebih lama dari batas tersebut kamu mungkin mengalami gangguan siklus menstruasi. Sangat disarankan untuk berkonsultasi dengan dokter untuk mengetahui masalah siklus menstruasimu.</p>
                </div>
                <h2 class="accordion">Gejala Yang Terjadi Saat Masa Menstruasi</h2>
                <div class="content">   
                    <p>Gejala yang terjadi saat masa menstruasi</p>
                    <ul>
                        <li>Perut terasa mulas, mual dan panas</li>
                        <li>Terasa nyeri saat buang air kecil</li>
                        <li>Tubuh tidak fit</li>
                        <li>Demam</li>
                        <li>Sakit kepala dan pusing</li>
                        <li>Mood swing</li>
                    </ul>

                    <br><p>Menstruasi biasanya disertai gejala dismenore, yaitu nyeri atau kram di perut bagian bawah yang sering kali diiringi keluhan seperti nyeri punggung, mual, muntah, diare, atau sakit kepala. Berdasarkan tingkat keparahannya, dismenore dibagi menjadi tiga tingkatan: nyeri ringan, nyeri sedang, dan nyeri berat.</p>
                    <ol>
                        <li>Dismenore ringan ditandai dengan rasa nyeri singkat di perut bagian bawah yang bersifat hilang timbul dan tidak mengganggu aktivitas sehari-hari. Penanganannya dapat dilakukan dengan istirahat, kompres hangat di area perut, olahraga ringan, atau konsumsi obat pereda nyeri ringan.</li>
                        <li>Dismenore sedang ditandai dengan nyeri yang lebih kuat dan dapat menjalar ke pinggang atau punggung. Meskipun aktivitas masih bisa dilakukan, nyeri dapat menghambat kenyamanan. Penanganan meliputi penggunaan obat pereda nyeri seperti ibuprofen, pijat ringan di area perut bawah, atau konsumsi air jahe untuk meredakan gejala.</li>
                        <li>Dismenore berat ditandai dengan nyeri yang sangat intens sehingga penderita tidak dapat melakukan aktivitas sehari-hari dan memerlukan waktu untuk beristirahat. Kondisi ini sering kali disertai gejala tambahan seperti mual, muntah, nyeri pinggang, dan sakit kepala. Jika nyeri berat tidak mereda dengan pengobatan sederhana, ini dapat terkait dengan kondisi medis seperti endometriosis atau adenomiosis. Penanganannya memerlukan evaluasi medis lebih lanjut, termasuk konsultasi dokter untuk terapi hormonal atau penggunaan obat pereda nyeri yang lebih kuat.</li>
                    </ol>
                </div>
            </div>

